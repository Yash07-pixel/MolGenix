    @staticmethod
    async def generate_report(
        target_context: TargetContext,
        molecules: List[Any],
        db: Any,
    ) -> Dict[str, Any]:
        """Generate a PDF report for a target context and molecule set."""
        from app.models.report import Report
        from app.models.target import Target

        target = db.query(Target).filter(Target.id == target_context.target_id).first()
        if not target:
            raise ValueError("Target not found")

        if not molecules:
            raise ValueError("molecules required for report generation")

        logger.info(
            "Generating report for target %s with %s molecules",
            target_context.target_id,
            len(molecules),
        )

        ordered_molecules = sorted(
            molecules,
            key=lambda molecule: (
                molecule.docking_score is None,
                molecule.docking_score if molecule.docking_score is not None else float("inf"),
            ),
        )[:5]
        report_target = ReportService._report_target(target_context)
        ordered_molecules = await ReportService._ensure_molecule_annotations(report_target, ordered_molecules, db)
        ReportService.REPORT_STORAGE_DIR.mkdir(parents=True, exist_ok=True)
        report = Report(
            target_id=target.id,
            molecule_ids=[str(molecule.id) for molecule in ordered_molecules],
            pdf_path="",
            file_size_bytes=0,
        )
        db.add(report)
        db.commit()
        db.refresh(report)

        buffer = io.BytesIO()
        ReportService._build_pdf(target_context, report_target, ordered_molecules, buffer)
        pdf_bytes = buffer.getvalue()
        report_path = ReportService.REPORT_STORAGE_DIR / f"{report.id}.pdf"
        report_path.write_bytes(pdf_bytes)
        report.pdf_path = str(report_path)
        report.file_size_bytes = len(pdf_bytes)
        db.add(report)
        db.commit()
        db.refresh(report)

        return {
            "report_id": str(report.id),
            "pdf_url": f"/api/reports/{report.id}/download",
        }

    @staticmethod
    async def generate_report_for_target(
        target_id: str,
        db: Any,
        target_context: Any | None = None,
        molecule_ids: List[str] | None = None,
    ) -> Dict[str, Any]:
        """Backward-compatible wrapper that assembles report inputs from persisted rows."""
        from app.models.molecule import Molecule
        from app.models.target import Target

        target = db.query(Target).filter(Target.id == target_id).first()
        if not target:
            raise ValueError("Target not found")

        if not molecule_ids:
            raise ValueError("molecule_ids required for report generation")

        logger.info(
            "Generating report for target %s with %s molecule IDs: %s",
            target_id,
            len(molecule_ids),
            molecule_ids,
        )

        molecule_query = db.query(Molecule).filter(Molecule.target_id == target.id)
        molecule_uuids = [UUID(str(molecule_id)) for molecule_id in molecule_ids]
        molecule_query = molecule_query.filter(Molecule.id.in_(molecule_uuids))

        molecules = molecule_query.order_by(Molecule.docking_score.asc().nullslast(), Molecule.created_at.desc()).limit(5).all()
        if len(molecules) < 1:
            raise ValueError("No molecules found for target")

        context = ReportService._coerce_target_context(target_context or target)
        return await ReportService.generate_report(context, molecules, db)

