    @staticmethod
    def _molecule_image(smiles: str) -> io.BytesIO | None:
        if not RDKIT_AVAILABLE:
            return None

        mol = Chem.MolFromSmiles(smiles)
        if mol is None:
            return None

        image = Draw.MolToImage(mol, size=(300, 220))
        buffer = io.BytesIO()
        image.save(buffer, format="PNG")
        buffer.seek(0)
        return buffer

