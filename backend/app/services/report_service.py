    @staticmethod
    def _format_value(value: Any, digits: int = 2) -> str:
        if value in (None, "", "N/A"):
            return "N/A"
        if isinstance(value, (int, float)):
            return f"{value:.{digits}f}"
        return str(value)

    @staticmethod
    def _has_complete_admet(admet_scores: Dict[str, Any]) -> bool:
        if not isinstance(admet_scores, dict):
            return False
        return all(key in admet_scores for key in ReportService.REQUIRED_ADMET_KEYS)

    @staticmethod
    async def _ensure_molecule_annotations(target: Any, molecules: List[Any], db: Any) -> List[Any]:
        """Backfill ADMET and provenance fields for report rendering."""
        from app.models.molecule import Molecule
        from app.services.admet_service import ADMETService

        missing_admet_ids = [
            str(molecule.id)
            for molecule in molecules
            if not ReportService._has_complete_admet(molecule.admet_scores or {})
        ]
        if missing_admet_ids:
            await ADMETService.score_molecules(
                ReportService._coerce_target_context(target),
                [molecule for molecule in molecules if str(molecule.id) in missing_admet_ids],
                db,
            )

        refreshed: List[Any] = []
        for molecule in molecules:
            refreshed_molecule = db.query(Molecule).filter(Molecule.id == molecule.id).first() or molecule
            metadata = dict(refreshed_molecule.admet_scores or {})
            docking_meta = metadata.get("_docking")

            if refreshed_molecule.docking_score is not None and not isinstance(docking_meta, dict):
                metadata["_docking"] = {
                    "method": "legacy_stored_score",
                    "is_mock": False,
                    "fallback_reason": None,
                    "pdb_filename": "unknown",
                }
            elif refreshed_molecule.docking_score is None and not isinstance(docking_meta, dict):
                metadata["_docking"] = {
                    "method": "not_run",
                    "is_mock": False,
                    "fallback_reason": "not_run",
                    "pdb_filename": "N/A",
                }

            if metadata != (refreshed_molecule.admet_scores or {}):
                refreshed_molecule.admet_scores = metadata
                db.add(refreshed_molecule)

            refreshed.append(refreshed_molecule)

        if missing_admet_ids or any((molecule.admet_scores or {}).get("_docking") for molecule in refreshed):
            db.commit()
            for molecule in refreshed:
                db.refresh(molecule)

        return refreshed

    @staticmethod
    def build_target_description(tc: TargetContext) -> str:
        lines = []
        lines.append(f"{tc.protein_name} ({tc.gene_symbol}) is associated with {tc.disease}.")
        if tc.target_class and tc.target_class not in {"unknown", "other"}:
            lines.append(f"Target class: {tc.target_class}.")
        if tc.function and tc.function != "N/A":
            sentences = tc.function.split(". ")[:2]
            function_text = ". ".join(sentence.strip().rstrip(".") for sentence in sentences if sentence).strip()
            if function_text:
                lines.append(function_text + ".")
        lines.append(
            f"ChEMBL target ID: {tc.chembl_id}. "
            f"UniProt accession: {tc.uniprot_id}. "
            f"{tc.known_inhibitors} known inhibitors identified in ChEMBL."
        )
        lines.append(
            f"Druggability score: {tc.druggability_score:.2f}/1.0. "
            f"Primary receptor structure used for docking: {tc.pdb_id}."
        )
        if tc.receptor_pdb_path and "tmp" in tc.receptor_pdb_path:
            lines.append("Note: Receptor structure was downloaded dynamically from RCSB for this run.")
        return " ".join(lines)

