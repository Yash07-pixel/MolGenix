    @staticmethod
    def build_confidence_assessment(
        target_context: TargetContext,
        docking_meta: dict,
        molecules: list,
        optimization_result: dict,
    ) -> dict:
        score_points = 0
        max_points = 0
        flags = []

        max_points += 3
        if docking_meta.get("receptor_prepared"):
            score_points += 2
        else:
            flags.append("Receptor not prepared - docking scores may be 20-30% less reliable")
        if docking_meta.get("binding_site_known"):
            score_points += 1
        else:
            flags.append("Binding site coordinates estimated - active site may be missed")

        max_points += 3
        best_score = min(
            (molecule.docking_score for molecule in molecules if molecule.docking_score is not None),
            default=0,
        )
        if best_score < -8:
            score_points += 3
        elif best_score < -7:
            score_points += 2
            flags.append("Docking scores moderate (< -7) - promising but not strong evidence")
        elif best_score < -6:
            score_points += 1
            flags.append("Docking scores weak (< -6) - experimental validation especially important")
        else:
            flags.append("Docking scores very weak - binding unlikely to be significant")

        max_points += 2
        if target_context.target_class not in ("unknown", "other"):
            score_points += 2
        else:
            flags.append("Target class not identified - molecule profiles are generic")

        max_points += 2
        if optimization_result and optimization_result.get("changes"):
            score_points += 2
        else:
            flags.append("No optimization performed - compounds are unrefined")

        model_sources = sorted(
            {
                str((molecule.admet_scores or {}).get("model_source")).strip()
                for molecule in molecules
                if (molecule.admet_scores or {}).get("model_source")
            }
        )
        if model_sources:
            flags.append(
                "ADMET values are ML predictions, not experimental measurements"
            )
        else:
            flags.append("ADMET provenance was not recorded clearly - treat values as predictive estimates")

        confidence_pct = int((score_points / max_points) * 100) if max_points else 0

        if confidence_pct >= 75:
            confidence_label = "HIGH - suitable for experimental follow-up"
        elif confidence_pct >= 50:
            confidence_label = "MODERATE - results are directionally useful"
        else:
            confidence_label = "LOW - treat as hypothesis generation only"

        return {
            "score": confidence_pct,
            "label": confidence_label,
            "flags": flags,
        }

